# Pętla 'for' jest używana, gdy znamy liczbę iteracji lub chcemy iterować po zbiorach (np. lista, zakres, itd.).
# Przykład: 'for i in range(10000)' iteruje dokładnie 10000 razy.

# Pętla 'while' jest używana, gdy warunek do zakończenia pętli zależy od jakiejś zmiennej, która może się zmieniać
# w trakcie działania pętli. Pętla 'while' działa dopóki warunek jest prawdziwy.
# Przykład: 
# i = 0
# while i < 10000:
#     print(i)
#     i += 1  # Zwiększamy i, aby pętla mogła zakończyć się.

# Główna różnica:
# - 'for' jest bardziej zwięzła i użyteczna, gdy znamy zakres lub liczbę iteracji z góry.
# - 'while' jest bardziej elastyczna i używana, gdy warunek zakończenia pętli zależy od zmieniających się danych.
